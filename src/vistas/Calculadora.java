    private void btnNumberMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNumberMousePressed
        // TODO add your handling code here:
        JButton botonActual = (JButton) evt.getComponent();
        if (resultado.equals("0")) {
            resultado = botonActual.getText();
        } else {
            resultado = resultado + botonActual.getText();
        }
        txtResultado.setText(resultado);
    }//GEN-LAST:event_btnNumberMousePressed

    private void btnCMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCMousePressed
        // TODO add your handling code here:
        if (!resultado.equals("0")) {
            if (resultado.length() == 1) {
                resultado = "0";
            } else {
                resultado = resultado.substring(0, resultado.length() - 1);
            }
        }
        txtResultado.setText(resultado);
    }//GEN-LAST:event_btnCMousePressed

    private void btnPuntoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPuntoMousePressed
        // TODO add your handling code here:
        if (!resultado.contains(".")) {
            resultado = resultado + ".";
        }
        txtResultado.setText(resultado);
    }//GEN-LAST:event_btnPuntoMousePressed

    private void btnCAMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCAMousePressed
        // TODO add your handling code here:
        resultado = "0";
        txtResultado.setText(resultado);
        primerNumero = 0;
        operador = '#';
    }//GEN-LAST:event_btnCAMousePressed

    private void btnMasMenosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnMasMenosMousePressed
        // TODO add your handling code here:
        if (resultado.charAt(0) == '-') {
            resultado = resultado.substring(1, resultado.length());
        } else {
            resultado = "-" + resultado;
        }
        txtResultado.setText(resultado);
    }//GEN-LAST:event_btnMasMenosMousePressed

    private void capturarOperacion(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_capturarOperacion
        // TODO add your handling code here:
        JButton botonActual = (JButton) evt.getComponent();
        primerNumero = Double.parseDouble(resultado);
        operador = botonActual.getText().charAt(0);
        resultado = "0";
        txtResultado.setText(resultado);
    }//GEN-LAST:event_capturarOperacion

    private void btnCalcularMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCalcularMousePressed
        // TODO add your handling code here:
        switch (operador) {
            case '/':
                if (!resultado.equals("0")) {
                    primerNumero = primerNumero / Double.parseDouble(resultado);
                } else {
                    JOptionPane.showMessageDialog(null, "No se puede dividir entre 0");
                }
                break;
            case '*':
                primerNumero = primerNumero * Double.parseDouble(resultado);
                break;
            case '-':
                primerNumero = primerNumero - Double.parseDouble(resultado);
                break;
            case '+':
                primerNumero = primerNumero + Double.parseDouble(resultado);
                break;
        }
        resultado = primerNumero + "";
        if (resultado.contains(".")) {
            String[] cadenaDividida = resultado.split("\\.");
            if (cadenaDividida[1].equals("0")) {
                resultado = cadenaDividida[0];
            }
        }
        txtResultado.setText(resultado);
    }//GEN-LAST:event_btnCalcularMousePressed

